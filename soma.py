# Programa que exibe o valor da soma dos números de uma lista:

numeros = [10, 20, 30, 40, 50]  # Aqui criamos a lista. 
soma = 0     # Variável soma. 
for numero in numeros:    # Para cada número dentro da lista "numeros", faça algo. Primeiro é 10, segundo é 20, terceiro é 30...
    soma += numero     # Significa "soma = soma + numero".
print(f"A soma total das receitas é: {soma}")      # Aparece a frase com a variável.

